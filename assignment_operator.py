# augmented assignment operator = evaluates the target and the expression list, performs the binary operation specific to the type of assignment on the two operands, and assigns the result to the original target.

friends = 10

# ADDITION & ASSIGNMENT
friends = friends + 1
friends += 1

# SUBTRACTION & ASSIGNMENT
friends = friends - 2
friends -= 2

# MULTIPLICATION & ASSIGNMENT
friends = friends * 3
friends *= 3

# DIVISION & ASSIGNMENT
friends = friends / 2
friends /= 2

# POWER & ASSIGNMENT
friends = friends ** 2
friends **= 2

# MODULO & ASSIGNMENT
remainder = friends % 3

print(friends)
print(remainder)